-- Default options that are always set: https://github.com/LazyVim/LazyVim/blob/main/lua/lazyvim/config/options.lua

--- Set tab and indent settings
vim.opt.tabstop = 2 -- A tab character will display as 2 spaces
vim.opt.shiftwidth = 2 -- Auto-indentation and << >> operations use 2 spaces
vim.opt.expandtab = true -- Insert spaces instead of tab characters
vim.opt.softtabstop = 2 -- When typing, a tab character (or the equivalent spaces) will be 2 spaces wide. Useful with expandtab.- Add any additional options here
